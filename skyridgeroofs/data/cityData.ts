export interface CityData {
  cityName: string;
  citySlug: string;
  state: string;
  phoneNumber: string;
  latitude: string;
  longitude: string;
  landmarks: Array<{
    name: string;
    address: string;
    url?: string;
    description?: string;
  }>;
  businessDistricts: Array<{
    name: string;
    description: string;
    link: string;
  }>;
  permitInfo: {
    cityName: string;
    phoneNumber: string;
    requirements: string;
  };
  contentSections: {
    overview: string;
    residentialServices: string[];
    commercialServices: string[];
    whyChooseUs: string;
    roofingServices: Array<{
      service: string;
      description: string;
      useCases: string[];
    }>;
    localInfo: string;
    serviceAreas: string;
  };
}

const DEFAULT_BUSINESS_PHONE = '801-252-6936';

interface GenericCityConfig {
  citySlug: string;
  cityName: string;
  latitude: string;
  longitude: string;
  county: string;
  neighborhoods: string[];
  serviceAreas: string;
  permitCityName?: string;
  permitPhone?: string;
  overviewNote?: string;
  landingPath?: string;
}

const toMapQuery = (value: string): string =>
  value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '+');

function createGenericCityData({
  citySlug,
  cityName,
  latitude,
  longitude,
  county,
  neighborhoods,
  serviceAreas,
  permitCityName,
  permitPhone,
  overviewNote,
  landingPath,
}: GenericCityConfig): CityData {
  const mapQuery = toMapQuery(cityName);
  const nearMe = citySlug.startsWith('me-');
  const districtNames =
    neighborhoods && neighborhoods.length > 0
      ? neighborhoods
      : ['Downtown Corridor', `${cityName} Residential District`, `${cityName} Commercial Hub`];
  const linkTarget = landingPath ?? (nearMe ? '/services' : `/locations/${citySlug}`);

  return {
    cityName,
    citySlug,
    state: 'UT',
    phoneNumber: DEFAULT_BUSINESS_PHONE,
    latitude,
    longitude,
    landmarks: [
      {
        name: `${cityName} City Hall`,
        address: `${cityName}, UT`,
        url: `https://www.google.com/maps/search/${mapQuery}+city+hall`,
        description: `Municipal services center supporting building permits for ${cityName} roofing projects.`,
      },
      {
        name: `${cityName} Public Library`,
        address: `${cityName}, UT`,
        url: `https://www.google.com/maps/search/${mapQuery}+public+library`,
        description: `Community resource hub where many homeowners research roofing contractors and materials.`,
      },
      {
        name: `${cityName} Community Park`,
        address: `${cityName}, UT`,
        url: `https://www.google.com/maps/search/${mapQuery}+park`,
        description: `Popular outdoor space that highlights the importance of quality roofing in ${county} weather conditions.`,
      },
    ],
    businessDistricts: districtNames.map((district) => ({
      name: district,
      description: `${district} is a frequent service area for Sky Ridge Roofing.`,
      link: linkTarget,
    })),
    permitInfo: {
      cityName: permitCityName ?? cityName,
      phoneNumber: permitPhone ?? DEFAULT_BUSINESS_PHONE,
      requirements: `Roofing projects in ${cityName} may require building permits. Our team coordinates with ${permitCityName ?? cityName} building department to ensure all permits are properly obtained before starting work.`,
    },
    contentSections: {
      overview:
        overviewNote ??
        `Sky Ridge Roofing provides expert roofing services throughout ${cityName} and ${county}. Our experienced team understands the unique weather challenges in this area and uses premium materials designed to withstand Utah's climate.`,
      residentialServices: [
        `Complete roof replacement in ${cityName}`,
        `Roof repairs and leak fixes across ${county}`,
        `Storm damage assessment and insurance claims`,
        `Gutter installation and repair`,
        `Solar panel installation and integration`,
      ],
      commercialServices: [
        `Commercial roof replacement throughout ${cityName}`,
        `Flat roof systems for office buildings`,
        `Retail storefront roofing projects`,
        `Industrial facility roofing solutions`,
        `Property management roofing services`,
      ],
      whyChooseUs: `We monitor weather patterns, coordinate with local building departments, and use premium materials to ensure ${cityName} roofing projects are completed on time and to the highest standards.`,
      roofingServices: [
        {
          service: 'Roof Replacement',
          description: `Complete roof replacement using premium materials designed for ${cityName}'s climate.`,
          useCases: ['Aged roofs over 20 years', 'Severe storm damage', 'Complete home renovation'],
        },
        {
          service: 'Roof Repairs',
          description: `Fast and reliable repairs for leaks, missing shingles, and general wear throughout ${county}.`,
          useCases: ['Leak repairs', 'Shingle replacement', 'Flashing repair', 'Emergency fixes'],
        },
        {
          service: 'Insurance Claims',
          description: `Expert assistance with insurance claims after storm damage or other covered events in ${cityName}.`,
          useCases: ['Storm damage assessment', 'Hail damage claims', 'Wind damage documentation', 'Insurance coordination'],
        },
      ],
      localInfo: `${cityName} experiences unique weather patterns within ${county}. We schedule around seasonal weather, coordinate with local building codes, and use materials specifically suited for this area's climate conditions.`,
      serviceAreas,
    },
  };
}

